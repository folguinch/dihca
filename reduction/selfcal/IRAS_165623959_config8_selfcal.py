field = 'IRAS_165623959'
flagchannels = "0:0~10;1277~1309;1771~1789;2311~2323;2342~2353;3256~3291;3830~3839,1:0~26;1635~1834;1911~1921;2793~2803;3830~3839,2:0~10;500~600;1000~1050;3830~3839,3:0~10;359~385;391~412;436~455;500~515;578~594;673~688;693~706;1070~1103;1733~1744;2000~2022;2304~2317;3830~3839"
